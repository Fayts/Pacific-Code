import type { NextConfig } from "next";

// En-têtes de sécurité appliqués à toutes les réponses. La CSP se limite à
// frame-ancestors (anti-clickjacking) : une CSP complète exigerait de gérer
// les scripts inline de Next et sera ajoutée avec le branchement cloud.
const securityHeaders = [
  { key: "X-Content-Type-Options", value: "nosniff" },
  { key: "X-Frame-Options", value: "DENY" },
  { key: "Content-Security-Policy", value: "frame-ancestors 'none'" },
  { key: "Referrer-Policy", value: "strict-origin-when-cross-origin" },
  {
    key: "Permissions-Policy",
    value: "camera=(), microphone=(), geolocation=()",
  },
  {
    key: "Strict-Transport-Security",
    value: "max-age=31536000; includeSubDomains",
  },
];

const nextConfig: NextConfig = {
  // Build autonome pour Docker : .next/standalone + server.js minimal.
  output: "standalone",
  async headers() {
    return [
      {
        source: "/(.*)",
        headers: securityHeaders,
      },
    ];
  },
};

export default nextConfig;
